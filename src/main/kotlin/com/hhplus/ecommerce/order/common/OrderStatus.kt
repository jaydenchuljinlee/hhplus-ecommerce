package com.hhplus.ecommerce.order.common

enum class OrderStatus {
    // ─── 결제 전 ──────────────────────────────────────────────
    REQUESTED,          // 주문 생성됨 — 재고 차감 이벤트 발행 대기
    STOCK_CONFIRMED,    // 재고 확보 완료 — 결제 진행 가능
    STOCK_FAILED,       // 재고 확보 실패 — 결제 불가, 주문 만료

    // ─── 결제 완료 ────────────────────────────────────────────
    CONFIRMED,          // 결제 완료 — 주문 최종 확정

    // ─── 배송 흐름 ────────────────────────────────────────────
    PREPARING,          // 상품 준비 중 (포장·출고 대기)
    READY_TO_SHIP,      // 출고 완료 — 택배사 인계 대기
    SHIPPED,            // 배송 시작 (택배사 접수)
    IN_TRANSIT,         // 배송 중
    DELIVERED,          // 배송 완료

    // ─── 구매 확정 ────────────────────────────────────────────
    PURCHASE_CONFIRMED, // 구매 확정 (수동 또는 자동 — 배송 완료 7일 후)

    // ─── 반품/환불 ────────────────────────────────────────────
    RETURN_REQUESTED,   // 반품 신청
    RETURNED,           // 반품 완료
    REFUND_PENDING,     // 환불 처리 중
    REFUNDED,           // 환불 완료

    // ─── 취소 ─────────────────────────────────────────────────
    CANCELED            // 주문 취소
}